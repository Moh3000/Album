<?php

namespace Album\Form;

use Laminas\Form\Form;

class AlbumForm extends Form
{
    public function __construct()
    {
        parent::__construct('album');
    

        $this->add([
            'name' => 'title',
            'type' => 'text',
            'options' => [
                'label' => 'Title',
            ],
        ]);

        $this->add([
            'name' => 'artist',
            'type' => 'text',
            'options' => [
                'label' => 'Artist',
            ],
        ]);

        $this->add([
            'name' => 'submit',
            'type' => 'submit',
            'attributes' => [
                'value' => 'Save',
            ],
        ]);
    }
}
